#include "Graphic.h"
#include "DxLib.h"
void Graphic::Set_Visible(bool in)
{
    visible = x;
}

void Graphic::Set_Graph(char* in)
{
    graph = LoadGraph(in);
}

void Graphic::Set_Location(int inx, int iny, int inxx, int inyy) {
    x = inx;
    y = iny;
    xx = inxx;
    yy = inyy;
    iniX = x;						// 左上の座標を格納
    iniY = y;
    iniXx = y;						//右下の座標を格納
    iniYy = y;
}

// 画像を表示する
void Graphic::Display() {
    if (visible) {
        DrawExtendGraph(x, y, xx, yy, graph, TRUE);
    }
}

